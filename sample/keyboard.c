void print_key(uint8_t scancode){
	static bool shift = false;
	uint8_t buf[2] = {0};


	switch(scancode){
		case 0x2a:
		case 0x36:
			shift = true;
			break;
		case 0xaa:
		case 0xb6:
			shift = false;
			break;
		default:
			if(!(scancode&0x80))
				_putc(shift ? keytable_s[scancode] : keytable_n[scancode]);
	}
}
